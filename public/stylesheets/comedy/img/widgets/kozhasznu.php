		A Komédium Közhasznú Nonprofit Kft.- t mint közhasznú kft-t  2009. augusztus 5. napján jegyezték be, jogutódlással a GT. hatályos rendelkezései alapján  a korábbi közhasznú társaságból, közhasznú nonprofit kft- ként folytatja működését.
		Jelen közhasznúsági jelentés tartalmazza az egyszerűsített mérlegbeszámolót és a kapott támogatások mértékét, annak felhasználását illetve rövid beszámolót a társaság közhasznú tevékenységéről a 2009. évről.
