	<p>A társaság közhasznú tevékenységéből befolyt bevételeket a fenti számok tükrözik.</p>
	<p>A színházban kávézó működik, saját alkalmazottal, amely ugyan vállalkozási tevékenységnek tekinthető, de a színházban ez kizárólag az előadásokhoz kapcsolódó szolgáltatás, tehát segíti a közhasznú tevékenységet. (Nyitva tartás kizárólag előadásnapokon, az előadások időpontjaihoz igazítva.) Így az egyéb minimális bevételeket is a közhasznú tevékenység bevételénél vesszük figyelembe.		</p>
	<p>A mérleg adataiból kitűnik, hogy a társaság eredménye: -998 eFt veszteség.</p>
	<p>A kapott céltámogatások - fent felsorolt módon - érkeztek a társasághoz, melyek mindegyikével a kapott összegek tételes elszámolásával a támogató felé az előírt határidőkre az elszámolást teljesítettük.</p>
	<p>A társaság vezető tisztségviselőinek nyújtott juttatások értéke (megbízási jogviszony alapján)</p>
